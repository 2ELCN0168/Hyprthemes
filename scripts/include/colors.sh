function _colors()
{
        R="\033[91m"
        G="\033[92m"
        Y="\033[93m"
        B="\033[94m"
        P="\033[95m"
        C="\033[96m"
        N="\033[0m"

        INFO="${C}[>]${N}"
        ERR="${R}[!]${N}"
        WARN="${Y}[@]${N}"
        SUC="${G}[*]${N}"
        QS="${B}::${N}"
        DB="${R}[DEBUG]${N}"
}
